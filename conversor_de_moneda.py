# Convertir dolares a pesos

dolares = float(input("Digite cantidad de dolares: $"))
trm = float(input("Digite trm actual: $"))

total_pesos = dolares * trm

print(f"Total dolares a pesos ${total_pesos}")

# Convertir pesos a dolares

pesos = float(input("Digite cantidad de pesos: $"))

total_dolares = pesos / trm

print(f"Total pesos a dolares ${total_dolares}")

